/** Render a brand line icon. */
export function Icon({ name, size = 24, strokeWidth = 2, color, style, ...rest }) {
  const node = PATHS[name];
  // solid glyphs paint with fill; line icons use stroke
  const solid = name === "whatsapp" || name === "facebook" || name === "star" || name === "instagram";
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={solid ? "currentColor" : "none"}
      stroke={solid ? "none" : "currentColor"}
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
      style={{ display: "block", color, flexShrink: 0, ...style }}
      {...rest}
    >
      {node}
    </svg>
  );
}

